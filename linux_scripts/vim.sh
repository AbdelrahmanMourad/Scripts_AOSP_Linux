# =============================================================================
# Vim Shortcuts Summary
# =============================================================================
# Basic Modes:
#   i       - Insert mode
#   Esc     - Normal mode
#   :w      - Save file
#   :q      - Quit Vim
#   :wq     - Save and quit
#   :q!     - Quit without saving
#
# Navigation:
#   h/j/k/l - Move left/down/up/right
#   gg      - Go to top of file
#   G       - Go to bottom of file
#   0/$     - Start/end of line
#   w/b     - Next/previous word
#
# Editing:
#   x       - Delete character
#   dd      - Delete line
#   yy      - Yank (copy) line
#   p       - Paste after cursor
#   u       - Undo
#   Ctrl+r  - Redo
#   r<char> - Replace character
#
# Visual Mode:
#   v       - Start visual mode (select text)
#   V       - Visual line mode
#   y       - Yank (copy) selection
#   d       - Delete selection
#
# Search & Replace:
#   /text   - Search for 'text'
#   n/N     - Next/previous search result
#   :%s/foo/bar/g - Replace all 'foo' with 'bar'
#
# Buffers & Windows:
#   :e file - Open file
#   :bn     - Next buffer
#   :bp     - Previous buffer
#   :sp     - Horizontal split
#   :vsp    - Vertical split
#   Ctrl+w  - Window commands (e.g., Ctrl+w, then h/j/k/l to move)
# =============================================================================

