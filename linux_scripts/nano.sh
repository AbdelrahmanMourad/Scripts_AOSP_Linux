# =============================================================================
# Nano Shortcuts Summary
# =============================================================================
# Basic Commands:
#   Ctrl+O   - Write (save) file
#   Ctrl+X   - Exit nano
#   Ctrl+R   - Insert file into current buffer
#   Ctrl+G   - Show help
#
# Navigation:
#   Ctrl+A   - Move to beginning of line
#   Ctrl+E   - Move to end of line
#   Ctrl+Y   - Page up
#   Ctrl+V   - Page down
#   Ctrl+_   - Go to line number
#
# Editing:
#   Ctrl+K   - Cut (delete) line
#   Ctrl+U   - Uncut (paste) line
#   Ctrl+J   - Justify (format) paragraph
#   Alt+6    - Copy current line (then Ctrl+U to paste)
#
# Search & Replace:
#   Ctrl+W   - Search
#   Ctrl+\\  - Search and replace
#   Alt+W    - Repeat last search
#
# Miscellaneous:
#   Ctrl+C   - Show current cursor position
#   Ctrl+T   - Spell check
# =============================================================================

